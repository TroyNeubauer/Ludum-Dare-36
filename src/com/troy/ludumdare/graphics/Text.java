package com.troy.ludumdare.graphics;

public class Text {
	
	public String text;
	public int x, y, size, style, color;

	public Text(String text, int x, int y, int size, int style, int color) {
		this.text = text;
		this.x = x;
		this.y = y;
		this.size = size;
		this.style = style;
		this.color = color;
	}
}
